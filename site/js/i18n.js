  // --- runtime ---
  function getLang() {
    try { return localStorage.getItem("dv-lang") || "en"; } catch (e) { return "en"; }
  }
  function setLang(code) {
    try { localStorage.setItem("dv-lang", code); } catch (e) {}
    apply(code);
    updateSwitcherLabel(code);
    var langObj = LANGS.find(function (l) { return l.code === code; });
    document.documentElement.setAttribute("lang", code + (code === "ar" ? "-SA" : ""));
    document.documentElement.setAttribute("dir", (langObj && langObj.rtl) ? "rtl" : "ltr");
  }
  function t(key, code) {
    var dict = T[code] || T.en;
    return dict[key] || T.en[key] || "";
  }
  function apply(code) {
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var key = el.getAttribute("data-i18n");
      if (key.endsWith(".html")) { el.innerHTML = t(key, code); }
      else { el.textContent = t(key, code); }
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder"), code));
    });
  }

  // --- switcher UI ---
  function buildSwitcher() {
    var host = document.querySelector(".lang-switcher");
    if (!host) return;
    var current = getLang();
    var btn = document.createElement("button");
    btn.className = "lang-btn";
    btn.type = "button";
    btn.setAttribute("aria-haspopup", "listbox");
    btn.setAttribute("aria-expanded", "false");
    btn.innerHTML = '<svg viewBox="0 0 24 24" width="16" height="16" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="10"/><path d="M2 12h20M12 2a15 15 0 0 1 0 20M12 2a15 15 0 0 0 0 20"/></svg><span class="lang-label"></span><svg class="lang-caret" viewBox="0 0 24 24" width="12" height="12" fill="none" stroke="currentColor" stroke-width="2.4" stroke-linecap="round" stroke-linejoin="round"><path d="M6 9l6 6 6-6"/></svg>';
    var list = document.createElement("ul");
    list.className = "lang-menu";
    list.setAttribute("role", "listbox");
    LANGS.forEach(function (l) {
      var li = document.createElement("li");
      li.setAttribute("role", "option");
      li.setAttribute("data-code", l.code);
      li.innerHTML = '<span class="lang-flag">' + l.flag + '</span><span class="lang-native">' + l.native + '</span><span class="lang-en">' + l.name + '</span>';
      if (l.code === current) li.classList.add("on");
      li.addEventListener("click", function () {
        setLang(l.code);
        list.querySelectorAll("li").forEach(function(x){x.classList.remove("on")});
        li.classList.add("on");
        close();
      });
      list.appendChild(li);
    });
    host.appendChild(btn);
    host.appendChild(list);
    function open() { list.classList.add("open"); btn.setAttribute("aria-expanded","true"); }
    function close() { list.classList.remove("open"); btn.setAttribute("aria-expanded","false"); }
    btn.addEventListener("click", function (e) {
      e.stopPropagation();
      list.classList.contains("open") ? close() : open();
    });
    document.addEventListener("click", function (e) {
      if (!host.contains(e.target)) close();
    });
  }
  function updateSwitcherLabel(code) {
    var lang = LANGS.find(function (l) { return l.code === code; });
    if (!lang) return;
    var label = document.querySelector(".lang-btn .lang-label");
    if (label) label.textContent = lang.code.toUpperCase();
  }

  document.addEventListener("DOMContentLoaded", function () {
    buildSwitcher();
    setLang(getLang());
  });
